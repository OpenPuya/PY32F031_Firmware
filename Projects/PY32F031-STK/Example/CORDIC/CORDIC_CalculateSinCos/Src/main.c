/* Includes ------------------------------------------------------------------*/
#include "main.h"

/* Private define ------------------------------------------------------------*/
CORDIC_HandleTypeDef CordicHandle = {0};
CORDIC_ConfigTypeDef CordicCfg = {0};
CORDIC_CalculatedTypeDef CordicCalculate = {0};

/* Private variables ---------------------------------------------------------*/
/* Private user code ---------------------------------------------------------*/
/* Private macro -------------------------------------------------------------*/
#define PI   3.14159265358979323846
#define THETA (PI/6.0)
#define POWER_2_31 2147483648.0  /* To the 31st power of 2 */
/* Private function prototypes -----------------------------------------------*/
static void APP_SystemClockConfig(void);

/**
  * @brief  Main program.
  * @retval int
  */
int main(void)
{
  /* Reset of all peripherals, Initializes the Systick. */
  HAL_Init();
  
  /* System clock configuration */
  APP_SystemClockConfig(); 

  /* Initialize debugging serial port (used by printf) */
  BSP_USART_Config();
  
  /* CORDIC initialization */
  CordicHandle.Instance = CORDIC;
  HAL_CORDIC_Init(&CordicHandle);
  
  /* CORDIC configuration */
  CordicCfg.Function = CORDIC_FUNCTION_SIN_COS;
  CordicCfg.InSize = CORDIC_INSIZE_32BITS;
  CordicCfg.OutSize = CORDIC_OUTSIZE_32BITS;
  CordicCfg.StartMode = CORDIC_STARTMODE_NOT_AUTO;
  CordicCfg.Iterations = 24;
  HAL_CORDIC_Configure(&CordicHandle, &CordicCfg);
  
  /* Calculate sin and cos */
  CordicCalculate.Theta = (int32_t)((THETA / PI) * POWER_2_31);  /* Calculate sin(THETA) and cos(THETA) */
  if (HAL_CORDIC_Calculate(&CordicHandle, &CordicCfg, &CordicCalculate, 5) != HAL_OK)
  {
    APP_ErrorHandler();
  }

  /* Print calculate value */
  printf("sin value = %lf\r\n", (CordicCalculate.Sin / POWER_2_31));
  printf("cos value = %lf\r\n", (CordicCalculate.Cos / POWER_2_31));
  
  /* infinite loop */
  while (1)
  {
  }
}

/**
  * @brief  System clock configuration function
  * @param  None
  * @retval None
  */
static void APP_SystemClockConfig(void)
{
  RCC_OscInitTypeDef RCC_OscInitStruct = {0};
  RCC_ClkInitTypeDef RCC_ClkInitStruct = {0};

  /* Oscillator configuration */
  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_HSE | RCC_OSCILLATORTYPE_HSI | RCC_OSCILLATORTYPE_LSI | RCC_OSCILLATORTYPE_LSE; /* Select oscillator HSE, HSI, LSI, LSE */
  RCC_OscInitStruct.HSIState = RCC_HSI_ON;                          /* Enable HSI */
  RCC_OscInitStruct.HSIDiv = RCC_HSI_DIV1;                          /* HSI 1 frequency division */
  RCC_OscInitStruct.HSICalibrationValue = RCC_HSICALIBRATION_8MHz;  /* Configure HSI clock 8MHz */
  RCC_OscInitStruct.HSEState = RCC_HSE_OFF;                         /* Close HSE */
  /*RCC_OscInitStruct.HSEFreq = RCC_HSE_16_32MHz;*/
  RCC_OscInitStruct.LSIState = RCC_LSI_OFF;                         /* Close LSI */
  RCC_OscInitStruct.LSEState = RCC_LSE_OFF;                         /* Close LSE */
  /*RCC_OscInitStruct.LSEDriver = RCC_LSEDRIVE_MEDIUM;*/
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_OFF;                     /* Close PLL */
  /*RCC_OscInitStruct.PLL.PLLSource = RCC_PLLSOURCE_HSI;*/
  /*RCC_OscInitStruct.PLL.PLLMUL = RCC_PLL_MUL2;*/
  /* Configure oscillator */
  if (HAL_RCC_OscConfig(&RCC_OscInitStruct) != HAL_OK)
  {
    APP_ErrorHandler();
  }

  /* Clock source configuration */
  RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK | RCC_CLOCKTYPE_SYSCLK | RCC_CLOCKTYPE_PCLK1; /* Choose to configure clock HCLK, SYSCLK, PCLK1 */
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_HSISYS; /* Select HSISYS as the system clock */
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;     /* AHB clock 1 division */
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV1;      /* APB clock 1 division */
  /* Configure clock source */
  if (HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_0) != HAL_OK)
  {
    APP_ErrorHandler();
  }
}

